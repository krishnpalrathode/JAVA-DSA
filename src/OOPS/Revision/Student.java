package OOPS.Revision;

public class Student {
   private int roll;
    private String name;
   private double per;

   public int getRoll(){
       return roll;
   }
   public String getName(){
       return name;
   }

   public double getPer(){
       return per;
   }

   public void setRoll(int roll){
       this.roll=roll;
   }

   public void setName(String name){
       this.name=name;
   }

   public void setPer(double per){
       this.per=per;
   }

}
