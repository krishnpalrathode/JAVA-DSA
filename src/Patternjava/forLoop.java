package Patternjava;

public class forLoop {
    public static void main(String[] args) {


        int arr[] = {10, 20, 30, 40, 50, 60};
        for(int i=0;i<=3;i++){
            System.out.print(arr[i]+" ");
        }
        System.out.println();
        for(int i=1;i<=3;i++){
            System.out.print(arr[i]+" ");
        }

    }
}